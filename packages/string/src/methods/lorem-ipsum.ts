/**
 * @param words The number of words to generate.
 * @returns A lorem ipsum string.
 *
 * @example
 * ```ts
 * loremIpsum(5) // "Lorem ipsum dolor sit amet"
 * ```
 */
export function loremIpsum(words: number = 128): string {
	return capitalize(
		Array.from({ length: words }, () => randomItem(loremIspumWords)).join(" ")
	);
}
